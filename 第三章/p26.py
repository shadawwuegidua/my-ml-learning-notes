import numpy as np
x = np.zeros((2,3,4))
print(x)
print()
# 假设 shape = (d0, d1, d2, d3)：

# Axis 0 (d0)：最外层的“块”或“组”的数量。
# Axis 1 (d1)：每个“块”里面包含的下一级子结构的数量。
# ...
# Axis -1 (最后一个)：最内层、也最基础的元素（通常指一行里的列数）。

# 假设有一个 shape 为 (2, 3, 4) 的数组。
# 你可以把它理解为 2 个大小为 3x4 的矩阵叠在一起。
x = np.ones((2,3,4),dtype = "uint32")
print(x)
print()

y = 10 * np.ones((2,3,4))
print(y)
print(y.dtype)
y = y.astype("uint8")
print(y)
print(y.dtype)

"""
【NumPy 数组的赋值、视图 (View) 与副本 (Copy)】
在处理 NumPy 数组时，理解底层数据是如何共享和复制的非常重要，这直接关系到内存占用以及如何避免意外篡改数据。
"""

a = np.arange(10)
print("原始数组 a:", a)

# 1. 简单赋值 (完全不复制)
# a 和 b 只是指向同一个 ndarray 对象的两个不同名字（引用）。
# 【重点】内存中并未产生新对象，改变其中一个，另一个受同样影响。
b = a
b[0] = 100
print("修改引用 b[0]=100 后 a 跟着变:", a) 

# 2. 副本 Copy (深拷贝)
# a.copy() 会在内存中开辟新空间，创建一个完整的、独立的新数组（包含全新的底层数据缓存）。
# 【重点】当你需要对数据进行破坏性计算/清洗，又必须保留原始数据作为对照时，必须使用 copy()。
c = a.copy()
c[0] = 200
print("修改副本 c[0]=200 后 a 保持不变:", a)  # a 的第一个元素仍是 100

# 3. 视图 View (浅拷贝) 
# a.view() 会创建一个全新的数组“对象”，但它与原数组「共享」相同的底层数据缓存。
# 【重点】修改视图的数据值，原数组也会随之改变。视图设计的核心是为了节省内存——当我们仅仅需要对数据进行 reshape(重塑形状) 或更换类型解读时，无需重写一遍庞大的数据。
d = a.view()
d[0] = 300
print("修改视图 d[0]=300 后 a 跟着变:", a)

# 4. 切片 Slice (返回视图)
# a[:] 在 Python 基础列表里是浅拷贝，但在 NumPy 中基础切片返回的是「视图」！
# 【重点】这是 NumPy 与原生 Python 列表最大的不同点之一！对长数组进行切片不会发生大通量的数据腾挪，只提供一个观察截取部分的“窗口”，修改切片里的元素等同于修改原数组。
e = a[:]
e[0] = 400
print("修改切片 e[0]=400 后 a 跟着变:", a)
